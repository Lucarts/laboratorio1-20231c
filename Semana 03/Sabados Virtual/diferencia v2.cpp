#include <iostream>
using namespace std;

int main() {
	/// declaracion
	float primerNumero;
	float segundoNumero;
	
	/// datos de entrada
	cout << "Ingrese primer numero: ";
	cin >> primerNumero;
	
	cout << "Ingrese segundo numero: ";
	cin >> segundoNumero;	
	
	/// datos de salida
	cout << "La diferencia es: " << (primerNumero - segundoNumero) << endl;
	
	
	return 0;
}

